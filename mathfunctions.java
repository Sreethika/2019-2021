public class mathfunctions
{
public static void main(String args[])
{
double m=6.25;
double n=5.55;
System.out.println("The output of different mathematical functions are");
System.out.println("Square root of 6.25 is "+Math.sqrt(m));
System.out.println("Minimum among 6.25 and 5.55 is "+Math.min(m,n));
System.out.println("Maximum among 6.25 and 5.55 is "+Math.max(m,n));
System.out.println("5.55 raised to power 4 is "+Math.pow(m,4));
System.out.println("Natural log value of 5.55 is "+Math.log(n));
System.out.println("Absolute value of 5.55 is "+Math.abs(n));
System.out.println("Rounded value of 6.25 is "+Math.round(m));
System.out.println("The floor value of 5.55 is "+Math.floor(n));
System.out.println("The ceil value of 6.25 is "+Math.ceil(m));
System.out.println("The rint value of 5.55 is "+Math.rint(n));
System.out.println("The exponential value of 6.25 is "+Math.exp(m));
}
}

